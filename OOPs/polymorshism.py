'''
Polymorphism
============

poly  => many
morphism => forms

A person plays different or many [poly] roles [forms] in his /her
lifer. This is called aspolymorphism in real life.

'''

a=10
b=20
c=a+b #When operands beside + are numbers then + is arithmetic operators
print("Addition of :",c)

x="Itvedant"
y="Eclass"

z=x+y #When operands beside + are string then + is concatenation operator
